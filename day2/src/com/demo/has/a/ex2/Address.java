package com.demo.has.a.ex2;

public class Address {

	private String doorNo;
	private String city;

	public Address() {

	}

	public void setDoorNo(String doorNo) {
		this.doorNo = doorNo;
	}

	public String getDoorNo() {
		return doorNo;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCity() {
		return city;
	}
}
